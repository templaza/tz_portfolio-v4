/**
 * Categories view class for the Category package.
 */
class HtmlView extends BaseHtmlView
{
    protected $items;

    /**
     * The pagination object
     *
     * @var  \Joomla\CMS\Pagination\Pagination
     */
    protected $pagination;

    /**
     * The model state
     *
     * @var   \Joomla\Registry\Registry
     */
    protected $state;

    /**
     * Form object for search filters
     *
     * @var  \Joomla\CMS\Form\Form
     */
    public $filterForm;

    /**
     * The active search filters
     *
     * @var  array
     */
    public $activeFilters;

    private $isEmptyState = false;

    /**
     * Display the view.
     */
    public function display($tpl = null) {

        $this -> state              = $this->get('State');
        $this -> item               = $this->get('Item');
        $canDo	                    = TZ_PortfolioHelper::getActions('addon', $this -> item -> id);
        $this -> canDo	= $canDo;

        $this -> form       = $this -> get('form');

        // Check for errors.
        if (count($errors = $this->get('Errors')))
        {
            throw new GenericDataException(implode("\n", $errors), 500);
        }

        if($this -> getLayout() == 'upload') {
            $this -> filterForm   = $this -> get('FilterForm');
        }

        $this->addToolbar();

        parent::display($tpl); // TODO: Change the autogenerated stub
    }

    /**
     * Add the page title and toolbar.
     *
     * @since	1.6
     */
    protected function addToolbar(){

        Factory::getApplication()->input->set('hidemainmenu', true);

        ToolbarHelper::title(Text::sprintf('COM_TZ_PORTFOLIO_TEMPLATES_MANAGER_TASK',Text::_('COM_TZ_PORTFOLIO_TEMPLATE_INSTALL_TEMPLATE')),'eye');
        ToolbarHelper::cancel($this -> getName().'.cancel',Text::_('JTOOLBAR_CLOSE'));

        ToolbarHelper::help('JHELP_CONTENT_ARTICLE_MANAGER',false,
            'https://www.tzportfolio.com/document/administration/35-how-to-use-templates-in-tz-portfolio-plus.html?tmpl=component');

        TZ_PortfolioToolbarHelper::customHelp('https://www.youtube.com/channel/UCrLN8LMXTyTahwDKzQ-YOqg/videos'
            ,'COM_TZ_PORTFOLIO_VIDEO_TUTORIALS', 'youtube', 'youtube');

        if($this -> getLayout() == 'upload') {
            ToolbarHelper::link('javascript:', Text::_('COM_TZ_PORTFOLIO_INTRO_GUIDE'), 'support');
        }
    }
}
