class DisplayController extends AddOnController {
    public function display($cachable = false, $urlparams = array())
    {
        $wa = Factory::getApplication() -> getDocument() -> getWebAssetManager();
//        $wa -> registerStyle('com_tz_portfolio.addon.content.vote',TZ_PortfolioUri::root(true)
//            .'/addons/content/vote/css/vote.css');

        $wa -> registerAndUseScript('com_tz_portfolio.addon.content.vote', TZ_PortfolioUri::root(true) . '/js/modernizr.custom.js',
            array('version' => '2.8.3', 'relative' => true));
        $wa -> registerAndUseScript('com_tz_portfolio.addon.content.vote', TZ_PortfolioUri::root(true) . '/js/classie.min.js',
            array('version' => 'auto', 'relative' => true));
        $wa -> registerAndUseScript('com_tz_portfolio.addon.content.vote', TZ_PortfolioUri::root(true) . '/js/notificationfx.min.js',
            array('version' => 'v=1.0.0', 'relative' => true));

        $wa -> registerAndUseStyle('com_tz_portfolio.addon.content.vote', TZ_PortfolioUri::root(true)
            . '/add-ons/content/vote/css/vote.css', array('version' => 'auto'));
        $wa -> registerAndUseScript('com_tz_portfolio.addon.content.vote', TZ_PortfolioUri::root(true) . '/add-ons/content/vote/js/vote.min.js',
            array('version' => 'auto', 'relative' => true));
        return parent::display($cachable, $urlparams); // TODO: Change the autogenerated stub
    }
}